package com.example.blushfinance.fragments.Finance_Page;

public class IncomeCard {

    private String name;
    private int amount;


    public IncomeCard(String name, int amount) {
        this.name = name;
        this.amount = amount;

    }

    public String getName() {
        return name;
    }

    public String setName(String name) {
        this.name = name;
        return name;
    }

    public int setAmount(int amount) {

        this.amount = amount;
        return amount;

    }

    public int getAmount() {
        return amount;
    }














}
